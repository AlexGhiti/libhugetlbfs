#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <errno.h>
#include <sys/ipc.h>
#include <sys/shm.h>
#include <hugetlbfs.h>

#include "hugetests.h"

void remove_shmid(int shmid)
{
	if (shmid > 0) {
		if (shmctl(shmid, IPC_RMID, NULL) != 0)
			ERROR("shmctl(%x, IPC_RMID) failed (%s)\n",
			      shmid, strerror(errno));
		
	}
}
